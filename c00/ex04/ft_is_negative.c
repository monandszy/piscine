#include <unistd.h>

void	ft_is_negative(int i)
{
	char	p;
	char	n;

	p = 'P';
	n = 'N';
	if (i < 0)
	{
		write(1, &n, 1);
	}
	else if (i >= 0)
	{
		write(1, &p, 1);
	}
}
